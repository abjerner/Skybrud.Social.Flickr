namespace Skybrud.Social.Flickr.Enums {
    
    /// <summary>
    /// Enum class indicating the safety level of a photo.
    /// </summary>
    public enum FlickrPhotoSafetyLevel {

        /// <summary>
        /// Indicates that the safety level hasn't been specified.
        /// </summary>
        NotSpecified,

        /// <summary>
        /// Indicates that the safety level is safe.
        /// </summary>
        Safe,

        /// <summary>
        /// Indicates that the safety level is moderate.
        /// </summary>
        Moderate,

        /// <summary>
        /// Indicates that the safety level is restricted.
        /// </summary>
        Restricted

    }

}